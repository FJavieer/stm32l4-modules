	/**
	 * @brief	Test servos
	 * @return	Error
	 */
int	servo_test	(void)
{
	int	i;

	servo_init();

	for (i = 0; i <= 90; i+=10) {
		if (servo_test_position(-i)) {
			return	ERROR_NOK;
		}

		if (servo_test_position(0)) {
			return	ERROR_NOK;
		}

		if (servo_test_position(i)) {
			return	ERROR_NOK;
		}

		if (servo_test_position(0)) {
			return	ERROR_NOK;
		}
	}

	return	ERROR_OK;
}


/******************************************************************************
 ******* static functions (definitions) ***************************************
 ******************************************************************************/
static	int	servo_test_position	(float pos)
{
	if (servo_position_set(SERVO_S1, pos)) {
		return	ERROR_NOK;
	}
	if (servo_position_set(SERVO_S2, pos)) {
		return	ERROR_NOK;
	}
	if (servo_position_set(SERVO_S3, pos)) {
		return	ERROR_NOK;
	}
	if (servo_position_set(SERVO_S4, pos)) {
		return	ERROR_NOK;
	}

	if (delay_us(1000000u)) {
		return	ERROR_NOK;
	}

	return	ERROR_OK;
}



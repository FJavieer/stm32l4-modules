	/**
	 * @brief	Test LEDs
	 * @return	None
	 */
void	led_test	(void)
{
	int		i;

	for (i = 0; i <= 100; i++) {
		led_set();
		delay_us(1000u * i);

		led_reset();
		delay_us(1000u * i);
	}
}


